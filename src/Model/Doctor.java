package Model;

public class Doctor {
    int id;
    String skill;
    int price;
    String phone;
    String name;

    public Doctor(int id, String skill, int price, String phone, String name) {
        this.id = id;
        this.skill = skill;
        this.price = price;
        this.phone = phone;
        this.name = name;
    }
}
